    public void find() {
    	//1. 검색유형 알아내기(combo)
    	int n=combo.getSelectedIndex();
    	setTitle("n: "+n);
    	//2. 검색어 얻어오기
    	String keyword=tfSearch.getText();
    	
    	//3. 유효성 체크 (검색어 입력 안했을 경우)
    	if(keyword==null||keyword.trim().isEmpty()) {
    		//showMessage("검색어를 입력하세요");
    		//검색어를 입력하지 않으면 모든 글 가져오기
    		ArrayList<MemoVO> arr2=dao.listMemo();
    		showTable(arr2);
    		tfSearch.requestFocus();
    		return;
    	}
    	//4. dao.findMemo(검색유형, 검색어);==>반환값 받아서 (데이터)
    	ArrayList<MemoVO> arr=dao.findMemo(n, keyword);    	
    	showTable(arr);
    }
    
