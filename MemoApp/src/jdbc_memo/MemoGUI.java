    private void btWriteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btWriteActionPerformed
    		//1) tfName, tfMsg값 얻어오기
    		String name=tfName.getText();
    		String msg=tfMsg.getText();
    	
    		//2) 유효성 체크(사용자가 값을 입력 안했을 경우)
    		if(name==null||msg==null||name.trim().contentEquals("")
    				||msg.trim().isEmpty()) {
    			//null값과 빈문자열 체크
    			showMessage("모든 값을 입력하세요");
    			tfName.requestFocus();//입력 포커스 주기
    			return;
    		}
    	
    		//3) 1)얻은 값으로  MemoVO 객체를 생성해서  setting하기
    		MemoVO memo=new MemoVO(0,name,msg,null);
    	
    		//4) MemoDAO객체 생성후 insertMemo()호출하기
    		int cnt=dao.insertMemo(memo);
    		String str=(cnt>0)?"메모 등록 성공":"메모 등록 실패";
    		//5) 그 실행 결과에 따라 다이얼로그박스 보여주기
            showMessage(str);
            tfName.setText("");
            tfMsg.setText("");
            tfName.requestFocus();
            
            showList();//글목록 가져와 보여주기
    }//----------------------------------------
    
