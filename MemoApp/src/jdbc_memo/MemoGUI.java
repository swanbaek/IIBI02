    String[] colHeader= {"글번호","작성자","메모내용","작성일"};
    public void showTable(ArrayList<MemoVO> arr) {
    	//DefaultTableModel model=new DefaultTableModel(2차원배열,1차원배열)
    	Object[][]data=new Object[arr.size()][4];
    	//ArrayList에 있는 내용을 data에 옮기기
    	for(int i=0;i<data.length;i++) {
    		MemoVO memo=arr.get(i);
    		data[i][0]=memo.getIdx();
    		data[i][1]=memo.getName();
    		data[i][2]=memo.getMsg();
    		data[i][3]=memo.getWdate();
    	}
    	DefaultTableModel model=new DefaultTableModel(data,colHeader);
    	table.setModel(model);
    }//---------------------------------
    
    public void showList() {
    	ArrayList<MemoVO> arr=dao.listMemo();
    	showTaList(arr);
    }//---------------------------------
    
    public void showTaList(ArrayList<MemoVO> arr) {
    	if(arr==null) {
    		showMessage("SQL오류");
    		return;
    	}
    	if(arr.size()==0) {
    		taList.setText("데이터가 없습니다");
    	}else {
    		taList.setText("");
    		taList.append("=============================================\n");
    		taList.append("글번호\t작성자\t작성일\t메모내용\n");
    		taList.append("=============================================\n");
    		for(MemoVO m:arr) {
    			taList.append(m.getIdx()+"\t"+m.getName()+"\t"+m.getWdate());
    			taList.append("\t"+m.getMsg()+"\n");
    		}
    		taList.append("=============================================\n");
    	}
    }

